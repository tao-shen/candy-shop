  name: string;
  description: string;
  category: SkillCategory;
  icon: string;
  color: string;
  installCommand: string;  tags: string[];
  popularity: number;
  repo: string;
  skillMdUrl: string;
  config: Record<string, unknown>;
  greeting?: string;
